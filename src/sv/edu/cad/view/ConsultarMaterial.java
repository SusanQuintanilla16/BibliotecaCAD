    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        try
        {
            Conexion con = new Conexion(); //creacion del objeto
            con.setRs("select material.idMaterial, material.NombreMaterial, catalogo.Titulo from material \n" +
"inner join catalogo on material.idMaterial=catalogo.idMaterial where material.idMaterial= "+Integer.parseInt(txtID.getText()) );
            this.usuario=(ResultSet) con.getRs();
            usuario.next();
            llenarTextbox();
            while(Integer.parseInt(txtIDM.getText())!= this.usuario.getInt(1)){
                this.usuario.next();
            }
        }
        catch (SQLException ex)
        {
            Logger.getLogger(ConsultarMaterial.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

