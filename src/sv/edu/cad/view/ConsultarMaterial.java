    private void siguiente() throws SQLException{
    
        if(usuario.isAfterLast()==false){
            btnAnterior.setEnabled(true);
            usuario.next();
            llenarTextbox();
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Ya no existen mas registros para recorrer");
            usuario.previous();
            btnSiguiente.setEnabled(false);
        }
    }
    
    private void anterior() throws SQLException{
        if(usuario.isBeforeFirst()==false){
            btnSiguiente.setEnabled(true);
            usuario.previous();
            llenarTextbox();
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Ya no existen mas registros para recorrer");
            usuario.next();
            btnAnterior.setEnabled(false);
        }
    }
    
    private void llenarTextbox() throws SQLException
    { 
     txtIDM.setText(String.valueOf(usuario.getString(1)));
     txtMaterial.setText(usuario.getString(2));
     txtTitulo.setText(usuario.getString(3));     
    }

